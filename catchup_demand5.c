int catchup_get_info_demand5(struct catchup_t* cu)
{
  struct MemoryStruct postdata;
  struct MemoryStruct result;
  int res;
  AMFObject obj;
  char filename[32];
  struct curl_slist *slist=NULL; 

  postdata.memory = malloc(sizeof(demand5_bin));
  postdata.size = sizeof(demand5_bin);

  memcpy(postdata.memory,demand5_bin,sizeof(demand5_bin));
  memcpy(postdata.memory+0x1f7,cu->id,11);

  slist = curl_slist_append(slist, "Content-Type: application/x-amf");

  res = do_post("http://c.brightcove.com/services/messagebroker/amf?playerKey=8e8e110bb9d7d95eb3c3e500a86a21024eccd983",
                slist,&postdata,&result);

  curl_slist_free_all(slist); /* free the list again */ 

  if (res != 0)
    return 1;

  sprintf(filename,"%s.amf",cu->id);  
  int fd = open(filename,O_CREAT|O_TRUNC|O_RDWR,0644);
  write(fd,result.memory,result.size);
  close(fd);

#if 0
  int nRes = AMF_Decode(&obj, result.memory+6, result.size-6, TRUE);
  if (nRes < 0)
    {
      fprintf(stderr, "%s, error decoding invoke packet", __FUNCTION__);
      return 1;
    }

  int old_debug = RTMP_debuglevel;
  RTMP_debuglevel = RTMP_LOGDEBUG;
  AMF_Dump(&obj);
  RTMP_debuglevel = old_debug;
  exit(1);
#endif

  char* p = memmem(result.memory,result.size,"rtmpe://",8);

  if (!p) {
    fprintf(stderr,"No rtmpe URL found in result\n");
    return 1;
  }

  char* q = p;
  char* endp = result.memory+result.size;
  while ((*q >= ' ') && (q < endp))
    q++;

  *q = 0;
  fprintf(stderr,"**%s**\n",p);

  char* r = strstr(p,"mp4:");

  if (p==NULL)
    return 1;

  cu->protocol = RTMP_PROTOCOL_RTMPE;
  cu->flashVer = strdup("WIN 11,0,1,152");
  cu->playpath = strdup(r);
  *r = 0;

  p += 8; // Skip "rtmpe://"
  fprintf(stderr,"p=%s\n",p);
  q = index(p,'/');
  *q = 0;
  cu->host = p;
  q++;
  fprintf(stderr,"q=%s\n",q);
  r = rindex(q,'/');
  *r = 0;

  cu->app = strdup(q);

  cu->tcUrl = malloc(8+strlen(cu->host)+1+4+1+strlen(cu->app)+1);
  sprintf(cu->tcUrl,"rtmpe://%s:1935/%s",cu->host,cu->app);

  cu->port=1935;
  cu->conn = strdup("Z:");
  
  cu->swfVfy = strdup("http://admin.brightcove.com/viewer/us20111026.1813/connection/ExternalConnection_2.swf");

  return 0;
}

